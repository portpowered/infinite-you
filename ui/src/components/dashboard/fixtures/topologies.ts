function workStatePlace(
  workTypeId: string,
  stateValue: string,
  stateCategory: DashboardPlaceRef["state_category"],
): DashboardPlaceRef {
  return {
    place_id: `${workTypeId}:${stateValue}`,
    type_id: workTypeId,
    state_value: stateValue,
    kind: "work_state",
    state_category: stateCategory,
  };
}

